"""2.Compute the standard deviation of the NumPy array

 Input: arr = [20, 2, 7, 1, 34]"""

import numpy as np
arr = [20, 2, 7, 1, 34]
standard_deviation = np.std(arr)
print('standard_deviation of the numpy array is',standard_deviation)

"""
Output:
standard_deviation of the numpy array is 12.576167937809991
"""
